// Objeto Math
var x = 33.37;
console.log(Math.E.toPrecision(5)); // returns Euler's number
console.log(Math.PI.toFixed(3)); // returns PI
console.log(Math.SQRT2); // returns the square root of 2
console.log(Math.SQRT1_2); // returns the square root of 1/2
console.log(Math.LN2); // returns the natural logarithm of 2
console.log(Math.LN10); // returns the natural logarithm of 10
console.log(Math.LOG2E); // returns base 2 logarithm of E
console.log(Math.LOG10E); // returns base 10 logarithm of E
/* Métodos comuns*/
console.log(Math.round(x)); // Retorna x arredondado ao seu inteiro mais próximo
console.log(Math.ceil(x)); //Retorna x arredondado até o inteiro mais próximo
console.log(Math.floor(x)); //Retorna x arredondado até o inteiro mais próximo para baixo
console.log(Math.trunc(x)); //Retorna a parte inteiro de x (nova no ES6)